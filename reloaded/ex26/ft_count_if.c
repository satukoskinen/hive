int		ft_count_if(char **tab, int (*f)(char*))
{
	int		count;
	char	**p;

	p = tab;
	count = 0;
	while (*p != 0)
	{
		if (f(*p) == 1)
			count++;
		p++;
	}
	return (count);
}
