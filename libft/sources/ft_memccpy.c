#include <string.h>

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (((char*)src)[i] == (unsigned char)c)
			return (src++);
		((char*)dst)[i] = ((char*)src)[i];
		i++;
	}
	return (NULL);
}
